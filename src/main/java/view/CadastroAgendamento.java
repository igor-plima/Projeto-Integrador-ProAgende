    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String nomeSelecionado = cmbNome.getSelectedItem().toString();
        String servicoSelecionado = cmbServico.getSelectedItem().toString();
        String dataTexto = txtData.getText();
        String horaTexto = txtHora.getText();
        String status = cmbStatus.getSelectedItem().toString();

        // Validação
        if (nomeSelecionado.equals("Selecionar") || servicoSelecionado.equals("Selecionar") || dataTexto.isEmpty() || horaTexto.isEmpty() || status.equals("Selecionar")) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos!");
            return;
        }

        try {
            // Converter DATA
            java.time.format.DateTimeFormatter formatterData
                    = java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy");

            java.time.LocalDate data
                    = java.time.LocalDate.parse(dataTexto, formatterData);

            // Converter HORA
            java.time.format.DateTimeFormatter formatterHora
                    = java.time.format.DateTimeFormatter.ofPattern("HH:mm");

            java.time.LocalTime hora
                    = java.time.LocalTime.parse(horaTexto, formatterHora);

            // Formato correto para MySQL
            String dataHora = data.toString() + " " + hora.toString() + ":00";

            // Buscar o ID do cliente na lista que foi carregada
            int idCliente = 0;
            for (Cliente c : listaClientes) {
                if (c.getNome().equals(nomeSelecionado)) {
                    idCliente = c.getIdCliente();  // use getIdCliente() porque sua classe usa idCliente
                    break;
                }
            }

            // Buscar o ID do serviço na lista carregada
            int idServico = 0;
            for (Servico s : listaServicos) {
                if (s.getNome().equals(servicoSelecionado)) {
                    idServico = s.getIdServico();   // ← MUDE PARA getIdServico() aqui
                    break;
                }
            }

            // Verificação de segurança
            if (idCliente == 0 || idServico == 0) {
                JOptionPane.showMessageDialog(this,
                        "Não foi possível encontrar o ID do cliente ou serviço selecionado.\n"
                        + "Cliente selecionado: " + nomeSelecionado + "\n"
                        + "Serviço selecionado: " + servicoSelecionado,
                        "Erro de identificação",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            // DAO
            AgendamentoDAO agendamentoDAO = new AgendamentoDAO();
            agendamentoDAO.salvarAgendamento(idServico, idCliente, dataHora, status);

            JOptionPane.showMessageDialog(this, "Agendamento salvo com sucesso!");

            // Limpar campos
            cmbNome.setSelectedIndex(0);
            cmbServico.setSelectedIndex(0);
            txtData.setText("");
            txtHora.setText("");
            cmbStatus.setSelectedIndex(0);

        } catch (java.time.format.DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Data ou hora inválida!\nUse o formato correto.");
        }

    }//GEN-LAST:event_btnConfirmarActionPerformed

