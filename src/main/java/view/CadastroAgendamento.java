    //Método responsável por preencher o comboBox(Nome) da Tela com os dados do Banco de Dados
    private void carregarClientes() {
        ClienteDAO dao = new ClienteDAO();
        listaClientes = dao.listarTodos();

        cmbNome.removeAllItems();
        cmbNome.addItem("Selecionar");

        for (Cliente cliente : listaClientes) {
            cmbNome.addItem(cliente.getNome());   // ← MUDE PARA ISSO
        }
    }

    //Método responsável por preencher o outro comboBox(Servico) da Tela com os dados do Banco de Dados
    private void carregarServicos() {
        ServicoDAO dao = new ServicoDAO();
        listaServicos = dao.listarTodos();  // retorna List<Servico> com id + nome

        cmbServico.removeAllItems();
        cmbServico.addItem("Selecionar");

        if (listaServicos != null && !listaServicos.isEmpty()) {
            for (Servico servico : listaServicos) {
                cmbServico.addItem(servico.getNome());   // ← use getNome() para evitar erro de tipo
            }
        } else {
            System.out.println("Nenhum serviço encontrado no banco.");
        }
    }

