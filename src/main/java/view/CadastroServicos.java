    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Pegando os dados da tela
        String nome = txtNome.getText();
        String descricao = txtDescricao.getText();
        String precoTexto = txtPreco.getText();
        String duracaoTexto = txtDuracao.getText();
        String profissional = cmbProfissional.getSelectedItem().toString();
        
        // Validação de campos vazios
        if (nome.isEmpty() || descricao.isEmpty() || precoTexto.isEmpty() || duracaoTexto.isEmpty() || profissional.equals("Selecione")) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos!");
            return;
        }

        // Convertendo valores
        try {
            double preco = Double.parseDouble(precoTexto);
            int duracao = Integer.parseInt(duracaoTexto);

            // Convertendo profissional (nome → id)
            int idProfissional = 0;

            if (profissional.equals("João Silva")) {
                idProfissional = 1;
            } else if (profissional.equals("Maria Oliveira")) {
                idProfissional = 2;
            } else if (profissional.equals("Carlos Souza")) {
                idProfissional = 3;
            } else if (profissional.equals("Ana Lima")) {
                idProfissional = 4;
        }
            
            // Criando DAO
            ServicoDAO servicoDAO = new ServicoDAO();

            // Salvando no banco
            servicoDAO.salvarServico(idProfissional, nome, descricao, preco, duracao);

            // Mensagem de sucesso
            JOptionPane.showMessageDialog(this, "Serviço cadastrado com sucesso!");

            //Limpar campos após salvar
            txtNome.setText("");
            txtDescricao.setText("");
            txtPreco.setText("");
            txtDuracao.setText("");

            //Colocar o foco novamente no primeiro campo
            txtNome.requestFocus();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Preço deve ser número decimal e duração número inteiro ou texto!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

