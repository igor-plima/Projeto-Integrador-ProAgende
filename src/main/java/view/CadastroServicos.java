        //Método responsável por preencher o comboBox da Tela com os dados do Banco de Dados
        private void carregarProfissionais() {

            String sql = "SELECT nome FROM profissional";

            java.sql.Connection conn = null;
            java.sql.PreparedStatement stmt = null;
            java.sql.ResultSet rs = null;

            try {
                conn = Conexao.conectar();
                stmt = conn.prepareStatement(sql);
                rs = stmt.executeQuery();

                cmbProfissional.removeAllItems();
                cmbProfissional.addItem("Selecionar");

                while (rs.next()) {
                    cmbProfissional.addItem(rs.getString("nome"));
                }

            } catch (Exception e) {
                System.out.println("Erro ao carregar profissionais: " + e.getMessage());
            }
        }
    

